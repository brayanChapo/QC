package pe.qc.com.validator.presentacion.form.formSeccion;

public class FCreacionDirectorioNTFS {
	
	private String accion;
	private String ambiente;
	private String servidor;
	private String ruta;
	private String descripcion;
	private String permisosNTFS;
	
	public FCreacionDirectorioNTFS() {
		
	}

	public String getAccion() {
		return accion;
	}

	public void setAccion(String accion) {
		this.accion = accion;
	}

	public String getAmbiente() {
		return ambiente;
	}

	public void setAmbiente(String ambiente) {
		this.ambiente = ambiente;
	}

	public String getServidor() {
		return servidor;
	}

	public void setServidor(String servidor) {
		this.servidor = servidor;
	}

	public String getRuta() {
		return ruta;
	}

	public void setRuta(String ruta) {
		this.ruta = ruta;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getPermisosNTFS() {
		return permisosNTFS;
	}

	public void setPermisosNTFS(String permisosNTFS) {
		this.permisosNTFS = permisosNTFS;
	}
	
}
